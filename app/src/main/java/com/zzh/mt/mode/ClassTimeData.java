public class ClassTimeData {
    private String message;
    private String code;
    private String totalClassHoursRequired;
    private String startTime;
    private String haveClassHoursRequired;
    private String haveClassHoursElective;
    private String totalClassHoursElective;
    private String endTime;

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getTotalClassHoursRequired() {
        return totalClassHoursRequired;
    }

    public void setTotalClassHoursRequired(String totalClassHoursRequired) {
        this.totalClassHoursRequired = totalClassHoursRequired;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getHaveClassHoursRequired() {
        return haveClassHoursRequired;
    }

    public void setHaveClassHoursRequired(String haveClassHoursRequired) {
        this.haveClassHoursRequired = haveClassHoursRequired;
    }

    public String getHaveClassHoursElective() {
        return haveClassHoursElective;
    }

    public void setHaveClassHoursElective(String haveClassHoursElective) {
        this.haveClassHoursElective = haveClassHoursElective;
    }

    public String getTotalClassHoursElective() {
        return totalClassHoursElective;
    }

    public void setTotalClassHoursElective(String totalClassHoursElective) {
        this.totalClassHoursElective = totalClassHoursElective;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }
}
